const SocialSidebar = () => {
  const [isDark, setIsDark] = useState(true);
  const [isRtl, setIsRtl] = useState(false);

  useEffect(() => {
    const handleScroll = () => {
      // Find all light-background sections on the page
      const lightSections = document.querySelectorAll('[data-theme="light"]');
      const sidebarY = window.innerHeight / 2; // sidebar is vertically centered

      let overLight = false;
      lightSections.forEach((section) => {
        const rect = section.getBoundingClientRect();
        if (rect.top < sidebarY && rect.bottom > sidebarY) {
          overLight = true;
        }
      });

      setIsDark(!overLight);
    };

    window.addEventListener("scroll", handleScroll, { passive: true });
    handleScroll(); // check on mount
    return () => window.removeEventListener("scroll", handleScroll);
  }, []);

  // Watch for RTL changes on the document
  useEffect(() => {
    const checkDir = () => setIsRtl(document.documentElement.dir === 'rtl');
    checkDir();
    const observer = new MutationObserver(checkDir);
    observer.observe(document.documentElement, { attributes: true, attributeFilter: ['dir'] });
    return () => observer.disconnect();
  }, []);

  return (
    <aside
      className={`hidden lg:flex flex-col gap-[18px] fixed top-1/2 -translate-y-1/2 z-50 transition-all duration-500 ${
        isRtl ? 'left-6' : 'right-6'
      }`}
    >
      {socials.map((s) => (
        <a
          key={s.label}
          href={s.href}
          target="_blank"
          rel="noopener noreferrer"
          aria-label={s.label}
          className={`flex items-center justify-center hover:scale-110 transition-all duration-300 ${
            isDark
              ? "text-white/60 hover:text-white"
              : "text-black/40 hover:text-black"
          }`}
        >
          {s.icon}
        </a>
      ))}
    </aside>
  );
};

export default SocialSidebar;
